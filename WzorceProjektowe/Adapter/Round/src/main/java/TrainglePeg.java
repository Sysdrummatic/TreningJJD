class TrianglePeg {
private double height;
private double base;

    public TrianglePeg(){}

    public TrianglePeg(double height, double base){
        this.height = height;
        this.base = base;
    }

    public double getHeight() {
        return height;
    }

    public double getBase() {
        return base;
    }
}
